public class Tugas_A_Array1D {

    /**
     * @param args the command line arguments
     */
    public static void main(String[] args) {
        String[] nama = {"Alvin", "Pradana", "Antony"};
        System.out.print("Nama : ");
        for (int kata =0; kata < nama.length; kata++){
           
            System.out.print(nama[kata]);
            System.out.print(" ");
        }
        System.out.println("");
    }
    
} 
