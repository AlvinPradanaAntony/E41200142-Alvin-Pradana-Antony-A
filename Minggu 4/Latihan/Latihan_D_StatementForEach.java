public class Latihan_D_StatementForEach {

    // Statement - ForEach
    public static void main(String[] args) {
        // Membuat array
        int angka[] = {3, 1, 4, 24, 12};

        // Menggunakan perulangan For each untuk menampilkan angka
        for (int x : angka) {
            System.out.print(x + " ");
        }
    }

}
